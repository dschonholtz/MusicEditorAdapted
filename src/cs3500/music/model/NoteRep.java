package cs3500.music.model;

/** A representation of a musical note */
public interface NoteRep {
    /** @return the beat of the song that this note starts on */
    int getStart();
    /** @return the number of beats this note lasts */
    int getDuration();
    /** @return the beat of the song that this note ends on */
    int getEnd();
    /** @return the pitch of this note */
    Pitch getPitch();
    /** @return the octave this note is in */
    int getOctave();
    /** @return the string representation of this note in the form PitchOctave */
    String toString();
    /** @return the volume of the note */
    int getVolume();
    /** @return the instrument number */
    int getInstrument();
    /** @return this NoteRep */
    NoteRep returnNote();
}
